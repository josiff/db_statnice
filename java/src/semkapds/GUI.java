    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed

        if (tableZapS.getSelectedRow() > -1) {

            int dialogResult = JOptionPane.showConfirmDialog(rootPane, "Vymazať záznam?", "Pozor", JOptionPane.YES_NO_OPTION);
            if (dialogResult == JOptionPane.YES_OPTION) {

                String str = (String) tableZapS.getModel().getValueAt(tableZapS.getSelectedRow(), 0);
                int student = Integer.parseInt(str);
                service.OdstranZapStud(student, getSelecI(cbKomisie));
            }
        }

    }//GEN-LAST:event_jButton10ActionPerformed

