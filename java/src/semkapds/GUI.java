    private void jTabbedPane1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabbedPane1MouseClicked

        switch (jTabbedPane1.getSelectedIndex()) {

            case 0:
                cbStupen.setEnabled(true);
                cbOdbor.setEnabled(true);
                cbZameranie.setEnabled(false);
                break;
            case 1:
                cbStupen.setEnabled(false);
                cbOdbor.setEnabled(false);
                cbZameranie.setEnabled(false);
                break;
            case 2:
                cbStupen.setEnabled(true);
                cbOdbor.setEnabled(true);
                cbZameranie.setEnabled(true);
                break;

        }


    }//GEN-LAST:event_jTabbedPane1MouseClicked

